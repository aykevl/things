var xorstate uint32 = 0xb90ccbd4 // just a random number to start

func rand() uint32 {
	xorstate = xorshift32(xorstate)
	return xorstate
}

func xorshift32(state uint32) uint32 {
	state *= 0x9ab4ffa3 // just a random number
	state ^= state << 13
	state ^= state >> 17
	state ^= state << 5
	return state
}
